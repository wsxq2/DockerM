package top.wsxq2.dockerm.model.portainer;

public class Team extends DataItem {
    private String name;

    public Team(int id, String name) {
        super(id);
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }
}
